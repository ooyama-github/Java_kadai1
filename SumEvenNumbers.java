// ２から１００までの偶数のみをすべて足す計算プログラム


public class SumEvenNumbers {
    public static void main(String[] args) {
        int sum = 0;
        for (int i = 2; i <= 100; i += 2) {
            sum += i;
        }
        System.out.println("2から100までの偶数の合計は " + sum);
    }
}


// ～解説～
// 'SumEvenNumbers'クラスを定義します。
// 'main'メソッド定義します。
// 'int'型の'sum'定義し、初期化。合計値。
// for ループを使用して、2から100までの偶数を1つずつ加算していきます。
// i という名前の int 型の変数を初期化して、2から開始します。
// i が100以下である限り、ループを続け、ループの反復で i を2ずつ増加させます。
// 反復で、現在の値 'i' を sum変数に加算しています。
// 2から100までの偶数の合計値 sum を出力します。
// 文字列連結演算子 + を使用して、合計値を含むメッセージを作成しています。