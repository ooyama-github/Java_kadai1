// １から１００までの整数をすべて足す計算プログラムです。

public class SumOfNumbers {
  public static void main(String[] args) {
    int sum = 0;

    for (int i = 1; i <= 100; i++) {
      sum += i;
    }

    System.out.println("1から100までの整数の合計は" + sum + "です。");
  }
}


// ～解説～
// 'SumOfNumbers'という名前のクラス定義をします。
// 'main'メソッドを定義します。
// 'sum'という名前の'int'型の編集を初期化します。合計値。
// for ループを使用して、1から100までの整数を1つずつ加算していきます。
// i という名前の int 型の変数を初期化して、1から開始します。
// i が100以下である限り、ループを続け、ループの各反復で i を1ずつ増加させます。
// 反復で、現在の値 i を sum 変数に加算しています。
// 1から100までの整数の合計値 sum を出力します。
// 文字列連結演算子 + を使用して、合計値を含むメッセージを作成しています。